/**
 * Returns the ISO 639-1 code of the desiredLang – if it is supported by Google Translate
 * @param {string} desiredLang – the name or the code of the desired language
 * @returns {string|boolean} The ISO 639-1 code of the language or false if the language is not supported
 */
function getCode(desiredLang) {
    desiredLang = desiredLang.toString().toLowerCase();

    for(var i = 0;i < langs.length;i++)
    {
        if(langs[i].name.toLowerCase() == desiredLang)
            return langs[i].language
    }
    return false
}

/**
 * Returns the weight of the desiredLang – if it is supported by Google Translate
 * @param {string} desiredLang – the name or the code of the desired language
 * @returns {string|boolean} The weight of the language or false if the language is not supported
 */
function getWeight(desiredLang) {
    if (!desiredLang) {
        return false;
    }
    desiredLang = desiredLang.toLowerCase();

    if (langs[desiredLang]) {
        return desiredLang;
    }

    for(var i = 0;i < langs.length;i++)
    {
        if(langs[i].name.toLowerCase() == desiredLang)
            return langs[i].score
    }
    return false
}

